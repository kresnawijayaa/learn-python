# operator aritmatika

# + operasi tambah
result_tambah = 1 + 2
print(result_tambah)

# unary+ penanda nilai positif
num_positif = +2
print(num_positif)

# - operasi kurang
result_kurang = 5 - 1
print(result_kurang)

# unary+ penanda nilai negatif
num_negatif = -2
print(num_negatif)

# * operasi kali
result_kali = 5 * 2
print(result_kali)

# / operasi bagi
result_bagi = 6 / 2
print(result_bagi)

# // operasi bagi tapi dibulatkan kebawah
result_bagi_bawah = 5 // 2
print(result_bagi_bawah)

# % operasi modulo (sisa hasil bagi)
result_modulo = 7 % 4
print(result_modulo)

# ** operasi pangkat
result_pangkat = 3 ** 2
print(result_pangkat)