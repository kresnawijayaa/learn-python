# operator perbandingan

# outputnya adalah boolean, true atau false

# == apakah kiri sama dengan kanan
res_1 = 4 == 5
print(res_1)

# != apakah kiri tidak sama dengan kanan
res_2 = 4 != 5
print(res_2)

# > apakah kiri lebih besar dibanding kanan
res_3 = 4 > 5
print(res_3)

# < apakah kiri lebih kecil dibanding kanan
res_4 = 4 < 5
print(res_4)

# >= apakah kiri lebih besar atau sama dengan kanan
res_5 = 4 > 5
print(res_5)

# <= apakah kiri lebih kecil atau sama dengan kanan
res_6 = 4 <= 5
print(res_6)